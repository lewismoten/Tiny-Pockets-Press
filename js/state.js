TPP.hydrateBookDates = function (book) {
  const now = TPP.nowIso();
  const fallbackMeta = (TPP.fallbackBook && TPP.fallbackBook().meta) || {};
  if (
    !Number.isFinite(Number(book.schemaVersion)) ||
    Number(book.schemaVersion) < 1
  )
    book.schemaVersion = 1;
  book.schemaVersion = Math.max(1, Math.floor(Number(book.schemaVersion) || 1));
  const meta = Object.assign({}, fallbackMeta, book.meta || {});
  if (book.id && !meta.id) meta.id = book.id;
  if ("revision" in book && !("revision" in (book.meta || {})))
    meta.revision = book.revision;
  if ("subrevision" in book && !("subrevision" in (book.meta || {})))
    meta.subrevision = book.subrevision;
  if (!Array.isArray(meta.provenance) && Array.isArray(book.provenance))
    meta.provenance = TPP.clone(book.provenance);
  if (!Array.isArray(meta.provenance) && Array.isArray(book.ancestry))
    meta.provenance = TPP.clone(book.ancestry);
  if ("createdAt" in book && !meta.createdAt) meta.createdAt = book.createdAt;
  if ("updatedAt" in book && !meta.updatedAt) meta.updatedAt = book.updatedAt;
  if ("lastExportedAt" in book && !("lastExportedAt" in (book.meta || {})))
    meta.lastExportedAt = book.lastExportedAt;
  if ("lastImportedAt" in book && !("lastImportedAt" in (book.meta || {})))
    meta.lastImportedAt = book.lastImportedAt;
  if ("coverPreviewId" in book && !meta.coverPreviewImageId)
    meta.coverPreviewImageId = book.coverPreviewId;
  if ("coverPreviewImageId" in book && !meta.coverPreviewImageId)
    meta.coverPreviewImageId = book.coverPreviewImageId;
  if ("_pageCount" in book && !("_pageCount" in (book.meta || {})))
    meta._pageCount = book._pageCount;
  if (!meta.id) meta.id = TPP.uid();
  if (!Number.isFinite(Number(meta.revision)) || Number(meta.revision) < 1)
    meta.revision = 1;
  meta.revision = Math.max(1, Math.floor(Number(meta.revision) || 1));
  if (
    !Number.isFinite(Number(meta.subrevision)) ||
    Number(meta.subrevision) < 0
  )
    meta.subrevision = 0;
  meta.subrevision = Math.max(0, Math.floor(Number(meta.subrevision) || 0));
  if (!Array.isArray(meta.provenance)) meta.provenance = [];
  delete book.ancestry;
  if (!meta.createdAt && meta.updatedAt) meta.createdAt = meta.updatedAt;
  if (!meta.createdAt) meta.createdAt = now;
  if (!meta.updatedAt) meta.updatedAt = meta.createdAt;
  if (!("lastExportedAt" in meta)) meta.lastExportedAt = "";
  if (!("lastImportedAt" in meta)) meta.lastImportedAt = "";
  if (!("coverPreviewImageId" in meta)) meta.coverPreviewImageId = "";
  meta._pageCount = Math.max(0, Math.floor(Number(meta._pageCount) || 0));
  book.meta = meta;
  [
    "id",
    "revision",
    "subrevision",
    "provenance",
    "createdAt",
    "updatedAt",
    "lastExportedAt",
    "lastImportedAt",
    "coverPreviewId",
    "coverPreviewImageId",
    "_pageCount",
  ].forEach(function (key) {
    delete book[key];
  });
  TPP.compactBookMeta(book);
  book.schemaVersion = TPP.SCHEMA_VERSION;
  return book;
};
TPP.unwrapImportPayload = function (data) {
  if (!data || typeof data !== "object") return { kind: "book", value: data };
  if (Array.isArray(data.books))
    return {
      kind: "library",
      value: data.books,
      schemaVersion: Number(data.schemaVersion) || 1,
    };
  if (data.book && typeof data.book === "object")
    return {
      kind: "book",
      value: data.book,
      schemaVersion:
        Number(data.schemaVersion) || Number(data.book.schemaVersion) || 1,
    };
  if (data.style && typeof data.style === "object")
    return {
      kind: "style",
      value: data.style,
      schemaVersion: Number(data.schemaVersion) || 1,
    };
  return {
    kind: "book",
    value: data,
    schemaVersion: Number(data.schemaVersion) || 1,
  };
};
