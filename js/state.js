TPP.hashString = function (value) {
  let hash = 2166136261;
  const text = String(value || "");
  for (let i = 0; i < text.length; i++) {
    hash ^= text.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return ("0000000" + (hash >>> 0).toString(16)).slice(-8);
};
