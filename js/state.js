TPP.norm = function (book) {
  const base = TPP.fallbackBook();
  const out = Object.assign({}, base, book || {});
  out.signatureSize = TPP.signatureSize(out.signatureSize);
  out.sewingStations = TPP.sewingStations(out.sewingStations);
  out.sewingGuideOpacity = TPP.opacity(out.sewingGuideOpacity, 0.65);
  out.signatureGuideOpacity = TPP.opacity(out.signatureGuideOpacity, 0.65);
  out.imageExportDpi = TPP.dpi(out.imageExportDpi);
  out.mediaCaptionSize = TPP.mediaCaptionSize(
    out.mediaCaptionSize,
    base.mediaCaptionSize,
  );
  TPP.migrateCoverTextSettings(out, base);
  out.chapters =
    Array.isArray(out.chapters) && out.chapters.length
      ? out.chapters
      : base.chapters;
  out.chapters = out.chapters.map(function (chapter, index) {
    const normalized = Object.assign(
      {
        id: TPP.uid(),
        title: "Chapter " + (index + 1),
        text: "",
        imageId: "",
        imageElementId: "",
        imagePlacement: "none",
        imageZoom: 70,
        imageWidth: 70,
        imageRotate: 0,
        level: 0,
        isSubsection: false,
        isMetadata: false,
        includeInToc: true,
        tocTitle: "",
      },
      chapter,
    );
    TPP.migrateChapterInlineImage(out, normalized);
    normalized.imageZoom = Math.min(
      100,
      Math.max(10, Number(normalized.imageZoom || normalized.imageWidth) || 70),
    );
    normalized.imageWidth = normalized.imageZoom;
    return normalized;
  });
  TPP.migrateTextElements(out, base);
  TPP.migrateImageElements(out, base);
  TPP.syncLegacyTextFieldsFromElements(out);
  TPP.syncLegacyImageFieldsFromElements(out);
  TPP.hydrateBookDates(out);
  TPP.normalizeFiles(out);
  TPP.syncCoverPreviewAsset(out);
  const fileIds = new Set(
    out.files.map(function (file) {
      return file.id;
    }),
  );
  ["coverImageId", "backImageId", "spineImageId"].forEach(function (key) {
    if (out[key] && !fileIds.has(out[key])) out[key] = "";
  });
  (Array.isArray(out.imageElements) ? out.imageElements : []).forEach(
    function (element) {
      if (element && element.fileId && !fileIds.has(element.fileId))
        element.fileId = "";
    },
  );
  out.chapters.forEach(function (chapter) {
    if (chapter.imageId && !fileIds.has(chapter.imageId)) chapter.imageId = "";
  });
  TPP.syncLegacyImageFieldsFromElements(out);
  TPP.syncCoverPreviewAsset(out);
  return out;
};
TPP.load = async function () {
  try {
    TPP.library = JSON.parse(localStorage.getItem(TPP.LIB) || "[]");
  } catch {
    TPP.library = [];
  }
  if (!TPP.library.length) {
    let sample = null;
    try {
      sample = await fetch("data/sample-book.json").then(function (response) {
        return response.json();
      });
    } catch {
      sample = TPP.fallbackBook();
    }
    TPP.library = [TPP.norm(sample)];
    TPP.save();
  }
  TPP.library = TPP.library.map(TPP.norm);
  TPP.bookFingerprints = {};
  TPP.bookDraftFingerprints = {};
  TPP.library.forEach(function (book) {
    const fingerprint = TPP.bookFingerprint(book);
    const bookId = TPP.bookId(book);
    TPP.bookFingerprints[bookId] = fingerprint;
    TPP.bookDraftFingerprints[bookId] = fingerprint;
  });
  const activeId = localStorage.getItem(TPP.ACTIVE);
  TPP.active =
    TPP.library.find(function (book) {
      return TPP.bookId(book) === activeId;
    }) || TPP.library[0];
};
TPP.loadStaleKeyLookup = async function () {
  try {
    const response = await fetch("data/stale-key-lookup.json");
    if (!response.ok) throw new Error("lookup");
    const data = await response.json();
    TPP.staleKeyLookup =
      Array.isArray(data) && data.length
        ? data
        : TPP.clone(TPP.defaultStaleKeyLookup);
  } catch {
    TPP.staleKeyLookup = TPP.clone(TPP.defaultStaleKeyLookup);
  }
};
TPP.clearRevisionTimer = function (bookId) {
  if (!TPP.bookRevisionTimers[bookId]) return;
  clearTimeout(TPP.bookRevisionTimers[bookId]);
  delete TPP.bookRevisionTimers[bookId];
};
TPP.scheduleRevisionCommit = function (bookId, delay) {
  if (!bookId) return;
  TPP.clearRevisionTimer(bookId);
  TPP.bookRevisionTimers[bookId] = setTimeout(
    function () {
      const book = TPP.library.find(function (entry) {
        return TPP.bookId(entry) === bookId;
      });
      if (!book) return;
      TPP.save("commit", bookId);
    },
    Math.max(100, Number(delay) || 900),
  );
};
TPP.save = function (mode, bookId) {
  mode = mode || "commit";
  const targetIds = bookId
    ? [bookId]
    : TPP.library.map(function (book) {
        return TPP.bookId(book);
      });
  TPP.library.forEach(function (book) {
    const meta = TPP.bookMeta(book);
    const currentId = TPP.bookId(book);
    if (!targetIds.includes(currentId)) return;
    TPP.hydrateBookDates(book);
    const previous = TPP.bookFingerprints[currentId];
    const priorDraft = TPP.bookDraftFingerprints[currentId];
    const current = TPP.bookFingerprint(book);
    if (mode === "draft") {
      if (priorDraft !== current) {
        if (previous !== undefined && current !== previous) {
          meta.subrevision = TPP.bookSubrevision(book) + 1;
          meta.updatedAt = TPP.nowIso();
        } else {
          meta.subrevision = 0;
        }
      }
      TPP.bookDraftFingerprints[currentId] = current;
    } else {
      TPP.clearRevisionTimer(currentId);
      if (previous !== undefined && current !== previous) {
        meta.revision = TPP.bookRevision(book) + 1;
        meta.subrevision = 0;
        meta.updatedAt = TPP.nowIso();
      } else if (meta.subrevision) {
        meta.subrevision = 0;
      }
      TPP.bookFingerprints[currentId] = current;
      TPP.bookDraftFingerprints[currentId] = current;
    }
    TPP.compactBookMeta(book);
  });
  localStorage.setItem(TPP.LIB, JSON.stringify(TPP.library));
};
TPP.setActive = function (book) {
  TPP.active = book;
  localStorage.setItem(TPP.ACTIVE, TPP.bookId(book));
  TPP.loadForm();
  if (TPP.restoreReaderUi)
    TPP.restoreReaderUi(TPP.readSettingsUi ? TPP.readSettingsUi() : {});
  TPP.renderAll();
};
TPP.download = function (name, data) {
  const blob = new Blob([JSON.stringify(data, null, 2)], {
    type: "application/json",
  });
  TPP.downloadBlob(name, blob);
};
TPP.downloadBlob = function (name, blob) {
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = String(name).replace(/[/\\?%*:|"<>]/g, "-");
  link.click();
  URL.revokeObjectURL(url);
};
TPP.showProgress = function (pct, msg) {
  const wrap = document.getElementById("progress");
  const bar = document.getElementById("progressBar");
  const text = document.getElementById("progressText");
  if (!wrap || !bar || !text) return;
  wrap.hidden = false;
  bar.style.width = Math.max(2, Math.min(100, pct)) + "%";
  text.textContent = msg || "Rendering…";
  if (pct >= 100)
    setTimeout(function () {
      wrap.hidden = true;
    }, 800);
};
TPP.file = function (event, callback) {
  const file = event.target.files && event.target.files[0];
  if (!file) return;
  const reader = new FileReader();
  reader.onload = function () {
    callback(reader.result, file);
  };
  reader.readAsDataURL(file);
};
