TPP.combineProvenance = function (a, b) {
  const seen = new Set();
  return (Array.isArray(a) ? a : [])
    .concat(Array.isArray(b) ? b : [])
    .filter(function (entry) {
      const key = JSON.stringify(entry || {});
      if (seen.has(key)) return false;
      seen.add(key);
      return true;
    });
};
TPP.bookDescendant = function (source, overrides, action, stamp) {
  const when = stamp || TPP.nowIso();
  const descendant = TPP.norm(
    Object.assign({}, TPP.clone(source || {}), overrides || {}),
  );
  const meta = TPP.bookMeta(descendant);
  meta.id = (overrides && overrides.id) || meta.id || TPP.uid();
  meta.revision = 1;
  meta.subrevision = 0;
  meta.createdAt = when;
  meta.updatedAt = when;
  meta.lastExportedAt = "";
  meta.lastImportedAt = action === "import" ? when : "";
  meta.provenance = Array.isArray(TPP.bookProvenance(source))
    ? TPP.clone(TPP.bookProvenance(source))
    : [];
  meta.provenance.push(TPP.bookSourceEntry(source, action, when));
  return descendant;
};
TPP.bookImported = function (source, stamp) {
  const when = stamp || TPP.nowIso();
  const imported = TPP.norm(TPP.clone(source || {}));
  const meta = TPP.bookMeta(imported);
  meta.lastImportedAt = when;
  meta.provenance = TPP.combineProvenance(TPP.bookProvenance(imported), [
    TPP.bookSourceEntry(source, "import", when),
  ]);
  return imported;
};
TPP.mergeImportedBook = function (existing, incoming, stamp) {
  const when = stamp || TPP.nowIso();
  const merged = TPP.norm(
    Object.assign({}, TPP.clone(existing || {}), TPP.clone(incoming || {})),
  );
  const meta = TPP.bookMeta(merged);
  meta.id = TPP.bookId(existing) || meta.id;
  meta.createdAt = TPP.bookCreatedAt(existing) || meta.createdAt;
  meta.updatedAt = TPP.bookUpdatedAt(incoming) || meta.updatedAt;
  meta.revision = Math.max(
    1,
    TPP.bookRevision(existing),
    TPP.bookRevision(incoming),
  );
  meta.subrevision = 0;
  meta.lastImportedAt = when;
  meta.lastExportedAt =
    TPP.bookLastExportedAt(existing) || TPP.bookLastExportedAt(incoming) || "";
  meta.provenance = TPP.combineProvenance(
    TPP.combineProvenance(
      TPP.bookProvenance(existing),
      TPP.bookProvenance(incoming),
    ),
    [TPP.bookSourceEntry(incoming, "import", when)],
  );
  return merged;
};
